static inline const OLED_WeatherGlyphEntry_t *OLED_WeatherFontGet(uint16_t codepoint)
{
    uint16_t left = 0U;
    uint16_t right = (uint16_t)(sizeof(g_oled_weather_font_map) / sizeof(g_oled_weather_font_map[0]));

    while (left < right)
    {
        uint16_t mid = (uint16_t)(left + ((right - left) / 2U));
        uint16_t mid_code = g_oled_weather_font_map[mid].codepoint;

        if (mid_code == codepoint)
        {
            return &g_oled_weather_font_map[mid];
        }

        if (mid_code < codepoint)
        {
            left = (uint16_t)(mid + 1U);
        }
        else
        {
            right = mid;
        }
    }

    return 0;
}

static inline const uint8_t *OLED_WeatherFontLookup(uint16_t codepoint)
{
    const OLED_WeatherGlyphEntry_t *entry = OLED_WeatherFontGet(codepoint);
    return (entry != 0) ? entry->glyph : 0;
}

static inline uint8_t OLED_WeatherFontWidth(uint16_t codepoint)
{
    const OLED_WeatherGlyphEntry_t *entry = OLED_WeatherFontGet(codepoint);
    return (entry != 0) ? entry->width : 0U;
}

